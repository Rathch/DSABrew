/**
 * Kopiert Scriptorium-Pergament-Vorlagen aus `media/` nach `web/public/dsa/`
 * (NSC-Layout: bevorzugt media/npc-block-template.png mit Porträt-Transparenz, sonst media/foo.png).
 */
import { copyFileSync, mkdirSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const webRoot = join(__dirname, "..");
const repoRoot = join(webRoot, "..");
const outDir = join(webRoot, "public/dsa");

const copies = [
  ["media/image1.png", "list-bullet.png"],
  ["media/image24.png", "note-read-aloud-parchment.png"],
  ["media/image27.png", "note-gm-parchment.png"],
  ["media/image29.png", "note-gm-meister-bg.png"],
  ["media/image31.png", "note-gm-meister-trennlinie.png"],
  ["media/image30.png", "npc-parchment-texture.png"]
];

const PUNCH_FLAG = join(webRoot, ".npc-template-needs-punch");

mkdirSync(outDir, { recursive: true });

/** PNG-IHDR (Breite/Höhe) für npc-block-template-size.json */
function pngDimensions(absPath) {
  const b = readFileSync(absPath);
  if (b.length < 24 || Buffer.from(b.subarray(1, 4)).toString("ascii") !== "PNG") {
    return null;
  }
  return { w: b.readUInt32BE(16), h: b.readUInt32BE(20) };
}

for (const [srcRel, destName] of copies) {
  const from = join(repoRoot, srcRel);
  const to = join(outDir, destName);
  if (!existsSync(from)) {
    console.warn(`copy-parchment-assets: fehlt ${srcRel}`);
    continue;
  }
  copyFileSync(from, to);
  console.log(`copy-parchment-assets: ${destName}`);
}

const npcDest = join(outDir, "npc-block-template.png");
const npcPreferred = join(repoRoot, "media/npc-block-template.png");
const npcFallback = join(repoRoot, "media/foo.png");
let npcSourceForDims = null;
let needsPunch = false;
if (existsSync(npcPreferred)) {
  copyFileSync(npcPreferred, npcDest);
  npcSourceForDims = npcPreferred;
  needsPunch = false;
  console.log("copy-parchment-assets: npc-block-template.png ← media/npc-block-template.png");
} else if (existsSync(npcFallback)) {
  copyFileSync(npcFallback, npcDest);
  npcSourceForDims = npcFallback;
  needsPunch = true;
  console.log("copy-parchment-assets: npc-block-template.png ← media/foo.png (Loch per punch-npc-portrait-hole)");
} else {
  console.warn("copy-parchment-assets: fehlt media/npc-block-template.png und media/foo.png");
}
writeFileSync(PUNCH_FLAG, needsPunch ? "yes\n" : "no\n");

const templateOut = join(outDir, "npc-block-template.png");
if (existsSync(templateOut)) {
  const dim = pngDimensions(templateOut);
  if (dim) {
    writeFileSync(join(outDir, "npc-block-template-size.json"), `${JSON.stringify(dim)}\n`);
    console.log(`copy-parchment-assets: npc-block-template-size.json (${dim.w}×${dim.h})`);
  }
}

/** Breite/Höhe für <img width height> — aus der effektiv kopierten NSC-Vorlage */
if (npcSourceForDims) {
  const dim = pngDimensions(npcSourceForDims);
  if (dim) {
    const srcLabel = needsPunch ? "media/foo.png" : "media/npc-block-template.png";
    writeFileSync(
      join(webRoot, "src/npc-template-dimensions.ts"),
      `/** Auto-generated by copy-parchment-assets.mjs (IHDR von ${srcLabel}) */\nexport const NPC_TEMPLATE_PX = { w: ${dim.w}, h: ${dim.h} } as const;\n`
    );
    console.log(`copy-parchment-assets: npc-template-dimensions.ts (${dim.w}×${dim.h})`);
  }
}
